package org.equipe.services;

public class ModeloDTO {

    public String name;
    public String email;
    public String cargo;
    public boolean ativo;

    public String getName() {
        return name;
    }
    
    public String getEmail() {
        return email;
    }
    
    public String getCargo() {
        return cargo;
    }
    
    public boolean isAtivo() {
        return ativo;
    }
    
}
